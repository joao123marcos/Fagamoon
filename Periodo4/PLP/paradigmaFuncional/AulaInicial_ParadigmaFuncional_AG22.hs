--função soma
soma x y = x + y

-- função que incrementa o valor de entrada
incremento z = z + 1

--area do triangulo
areaTriangulo b h = (b*h)/ 2

--area circulo
areaCirculo r = pi * r * r

--Calcular a media
media x y z = (x + y + z)/3

--Calcular o dobro
dobro x = x*2

--Calcular o quadruplo
quadruplo x = (dobro x) * 2

--Calcular hipotenusa
hipotenusa x y = sqrt (x**2 + y**2)